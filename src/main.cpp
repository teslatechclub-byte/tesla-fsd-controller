// One-time migration from old verbose key names to obfuscated keys.
// Runs on first update; after migration old keys are removed.
static void migrateNvsKeys() {
    Preferences p;
    p.begin("fsd", false);
    if (!p.isKey(NV_FSD_EN) && p.isKey("fsdEn")) {
        p.putBool(NV_FSD_EN,   p.getBool("fsdEn",     false)); p.remove("fsdEn");
        p.putUChar(NV_HW_MODE, p.getUChar("hwMode",   2));     p.remove("hwMode");
        p.putUChar(NV_SP_PRO,  p.getUChar("spPro",    1));     p.remove("spPro");
        p.putBool(NV_PRO_AUTO, p.getBool("proAuto",   true));  p.remove("proAuto");
        p.putBool(NV_ISA_CHM,  p.getBool("isaChm",    false)); p.remove("isaChm");
        p.putBool(NV_EM_DET,   p.getBool("emDet",     true));  p.remove("emDet");
        p.putBool(NV_CN_MODE,  p.getBool("cnMode",    false)); p.remove("cnMode");
        p.putBool(NV_AP_RST,   p.getBool("apRestart", false)); p.remove("apRestart");
        // Legacy HW3 manual/smart/passthrough keys — feature removed in favor of
        // single hw3AutoSpeed toggle (see NV_HW3_AUTO). Drop any prior values.
        if (p.isKey("hw3Off"))   p.remove("hw3Off");
        if (p.isKey("hw3SmEn"))  p.remove("hw3SmEn");
        if (p.isKey("hw3SmT1"))  p.remove("hw3SmT1");
        if (p.isKey("hw3SmT2"))  p.remove("hw3SmT2");
        if (p.isKey("hw3SmT3"))  p.remove("hw3SmT3");
        if (p.isKey("hw3SmT4"))  p.remove("hw3SmT4");
        if (p.isKey("hw3SmO1"))  p.remove("hw3SmO1");
        if (p.isKey("hw3SmO2"))  p.remove("hw3SmO2");
        if (p.isKey("hw3SmO3"))  p.remove("hw3SmO3");
        if (p.isKey("hw3SmO4"))  p.remove("hw3SmO4");
        if (p.isKey("hw3SmO5"))  p.remove("hw3SmO5");
        if (p.isKey("precond")) p.remove("precond");  // feature removed
        if (p.isKey("apSSID")) { p.putString(NV_AP_SSID, p.getString("apSSID")); p.remove("apSSID"); }
        if (p.isKey("apPass")) { p.putString(NV_AP_PASS, p.getString("apPass")); p.remove("apPass"); }
        if (p.isKey("staSSID")){ p.putString(NV_STA_SSID,p.getString("staSSID"));p.remove("staSSID");}
        if (p.isKey("staPass")) { p.putString(NV_STA_PASS,p.getString("staPass"));p.remove("staPass");}
        DLOGLN("[NVS] migrated legacy keys");
    }
    // NV_OVR_SL moved "c5" → "c3" (c5 previously collided with NV_AP_PASS string
    // writes, producing garbage bool reads). We cannot reliably recover the user's
    // prior overrideSpeedLimit from "c5" because its contents may be string data.
    // On first boot after upgrade, default to safe=false and log.
    if (!p.isKey(NV_OVR_SL) && p.isKey(NV_FSD_EN)) {
        p.putBool(NV_OVR_SL, false);
        DLOGLN("[NVS] overrideSpeedLimit reset to false after c5→c3 migration");
    }
    // Remove retired obfuscated keys from the HW3 smart/manual/passthrough era.
    // These were all written by the previous firmware but the feature is gone.
    const char* retired[] = { "a8", "b1", "b2", "b3", "b4", "b5", "b6", "b7", "b8", "b9", "c1", "d3" };
    for (auto* k : retired) if (p.isKey(k)) p.remove(k);
    p.end();
    // Migrate PIN
    Preferences sec;
    sec.begin("sec", false);
    if (!sec.isKey(NV_PIN) && sec.isKey("pin")) {
        sec.putString(NV_PIN, sec.getString("pin")); sec.remove("pin");
    }
    sec.end();
}

void loadConfig() {
    prefs.begin("fsd", false);
    cfg.fsdEnable          = prefs.getBool(NV_FSD_EN,   false);
    cfg.hwMode             = prefs.getUChar(NV_HW_MODE, 2);
    cfg.speedProfile       = prefs.getUChar(NV_SP_PRO,  1);
    cfg.profileModeAuto    = prefs.getBool(NV_PRO_AUTO, true);
    cfg.isaChimeSuppress   = prefs.getBool(NV_ISA_CHM,  false);
    cfg.emergencyDetection = prefs.getBool(NV_EM_DET,   true);
    cfg.forceActivate      = prefs.getBool(NV_CN_MODE,  false);
    cfg.overrideSpeedLimit = prefs.getBool(NV_OVR_SL,   false);
    cfg.apRestart          = prefs.getBool(NV_AP_RST,   false);
    // Legacy NVS key wipe: older firmware wrote a precondition bool here. Remove
    // so it stops consuming NVS space. Feature was removed (needs Vehicle CAN).
    if (prefs.isKey(NV_PRECOND)) prefs.remove(NV_PRECOND);
    cfg.hw4OffsetRaw       = prefs.getUChar(NV_HW4_OFF, 0);
    if (cfg.hw4OffsetRaw > 15) cfg.hw4OffsetRaw = 15;  // v1.4.28: cap lowered 21→15; clamp stored values from older firmware
    cfg.trackModeEnable    = prefs.getBool(NV_TRACK_MD, false);
    cfg.hw3AutoSpeed       = prefs.getBool(NV_HW3_AUTO, true);
    cfg.hw3CustomSpeed     = prefs.getBool(NV_HW3_CUST, false);
    cfg.hw3OffsetSlew      = prefs.getBool(NV_HW3_SLEW, false);  // v1.4.27 opt-in test feature
    {
        uint8_t r = prefs.getUChar(NV_HW3_SLRT, kHw3SlewRateDefault);
        if (r < kHw3SlewRateMin || r > kHw3SlewRateMax) r = kHw3SlewRateDefault;
        cfg.hw3SlewRatePctPerSec = r;
    }
    cfg.hw3HighSpeedEnable   = prefs.getBool(NV_HW3_HSEN, false);
    {
        uint8_t v = prefs.getUChar(NV_LEG_OFF, 0);
        if (v > 33) v = 33;
        cfg.legacyOffset = v;
    }
    cfg.removeVisionSpeedLimit = prefs.getBool(NV_LEG_RVSL, true);
    {
        uint8_t enc = prefs.getUChar(NV_HW3_ENC, HW3_ENC_PCT4);
        if (enc > HW3_ENC_MAX) enc = HW3_ENC_PCT4;  // NVS corruption fallback
        cfg.hw3WireEncoding = enc;
    }
    cfg.ipBlockerEnabled = prefs.getBool(NV_IP_BLK, false);
    cfg.isaOverride      = prefs.getBool(NV_ISA_OVR, false);
    {
        uint8_t buf[kHw3HighSpeedBucketCount];
        if (prefs.getBytes(NV_HW3_HSPT, buf, sizeof(buf)) == sizeof(buf)) {
            for (int i = 0; i < kHw3HighSpeedBucketCount; i++) {
                if (buf[i] > 50) buf[i] = 50;  // Tesla fw cap
            }
            memcpy(const_cast<uint8_t*>(cfg.hw3HighSpeedTargetPct), buf, sizeof(buf));
        }
    }
    {
        // Read via temp to preserve in-class defaults on short/missing blob
        // (partial-length reads would leave the tail at 0 if we read directly).
        uint8_t buf[kHw3CustomTargetCount];
        if (prefs.getBytes(NV_HW3_CT, buf, sizeof(buf)) == sizeof(buf)) {
            memcpy(const_cast<uint8_t*>(cfg.hw3CustomTarget), buf, sizeof(buf));
        }
    }
    strlcpy(apSSID,  prefs.getString(NV_AP_SSID,  "FSD-Controller").c_str(), sizeof(apSSID));
    strlcpy(apPass,  prefs.getString(NV_AP_PASS,  "12345678").c_str(),       sizeof(apPass));
    strlcpy(staSSID, prefs.getString(NV_STA_SSID, "").c_str(),               sizeof(staSSID));
    strlcpy(staPass, prefs.getString(NV_STA_PASS, "").c_str(),               sizeof(staPass));

    prefs.end();

    // Load PIN from separate namespace
    Preferences secPrefs;
    secPrefs.begin("sec", false);
    strlcpy(storedPin, secPrefs.getString(NV_PIN, "").c_str(), sizeof(storedPin));
    secPrefs.end();

    // Clamp values
    if (cfg.hwMode > 2)       cfg.hwMode = 2;
    if (cfg.speedProfile > 4) cfg.speedProfile = 1;
}

