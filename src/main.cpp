    server.on("/api/scan/result", HTTP_GET, [](AsyncWebServerRequest* req) {
        if (!checkToken(req)) { req->send(403, "text/plain", "UNAUTH"); return; }
        int n = WiFi.scanComplete();
        if (n == WIFI_SCAN_RUNNING) {
            req->send(200, "application/json", "{\"scanning\":true}");
            return;
        }
        uint32_t up = (millis() - cfg.uptimeStart) / 1000;
        if (n <= 0) {
            char logMsg[48];
            snprintf(logMsg, sizeof(logMsg), "WiFi scan: %d networks (err=%d)", n, n);
            addDiagLog(up, logMsg);
            WiFi.scanDelete();
            req->send(200, "application/json", "[]");
            return;
        }
        char logMsg[48];
        snprintf(logMsg, sizeof(logMsg), "WiFi scan: %d networks found", n);
        addDiagLog(up, logMsg);
        String json = "[";
        for (int i = 0; i < n; i++) {
            if (i > 0) json += ",";
            String ssid = WiFi.SSID(i);
            ssid.replace("\\", "\\\\");
            ssid.replace("\"", "\\\"");
            json += "{\"ssid\":\"" + ssid + "\",\"rssi\":" + String(WiFi.RSSI(i)) + "}";
        }
        json += "]";
        WiFi.scanDelete();
        req->send(200, "application/json", json);
    });

