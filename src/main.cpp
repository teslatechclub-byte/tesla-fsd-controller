    // Set config — with input validation and NVS write-only-on-change
    server.on("/api/set", HTTP_GET, [](AsyncWebServerRequest* req) {
        if (!checkToken(req)) { req->send(403, "text/plain", "UNAUTH"); return; }

        // ── Validate ALL numeric params up-front; mutate only after every check passes.
        // Otherwise a later invalid param would leave cfg half-updated on 400.
        if (req->hasParam("hwMode")) {
            int raw = req->getParam("hwMode")->value().toInt();
            if (raw < 0 || raw > 2) { req->send(400, "text/plain", "bad hwMode"); return; }
        }
        if (req->hasParam("speedProfile")) {
            int raw = req->getParam("speedProfile")->value().toInt();
            if (raw < 0 || raw > 4) { req->send(400, "text/plain", "bad speedProfile"); return; }
            // Only HW4 (hwMode=2) supports extended profiles 3..4; HW3 and Legacy cap at 0..2.
            uint8_t curHw = req->hasParam("hwMode") ? (uint8_t)req->getParam("hwMode")->value().toInt() : cfg.hwMode;
            if (curHw != 2 && raw > 2) { req->send(400, "text/plain", "speedProfile out of range for non-HW4"); return; }
        }
        if (req->hasParam("hw4Offset")) {
            int raw = req->getParam("hw4Offset")->value().toInt();
            if (raw < 0 || raw > 15) {
                req->send(400, "text/plain", "bad hw4Offset"); return;
            }
        }
        // API-layer coarse filter only: reject negatives and absurd values for target
        // *speed* (0..200 kph). The +40 kph *offset* ceiling is an independent downstream
        // clamp in encodeHW3OffsetRawFromKph — target and offset are different quantities
        // (offset = target − fusedLimit), so they're checked separately.
        // Single-pass validate-into-tmp keeps all-or-nothing semantics: any bad param
        // 400s before we mutate cfg, so batched /api/set (UI debounces 5 inputs together)
        // never leaves RAM partially updated while NVS has the old blob.
        static const char* const kHw3CTKeys[kHw3CustomTargetCount] = {"hw3CT0","hw3CT1","hw3CT2","hw3CT3","hw3CT4"};
        uint8_t hw3CTNew[kHw3CustomTargetCount] = {0};
        bool    hw3CTHas[kHw3CustomTargetCount] = {false};
        for (int i = 0; i < kHw3CustomTargetCount; i++) {
            if (!req->hasParam(kHw3CTKeys[i])) continue;
            int raw = req->getParam(kHw3CTKeys[i])->value().toInt();
            if (raw < 0 || raw > 200) { req->send(400, "text/plain", "bad hw3CT"); return; }
            hw3CTNew[i] = (uint8_t)raw;
            hw3CTHas[i] = true;
        }
        // Same validate-before-apply dance for the ≥80 high-speed bucket table.
        static const char* const kHw3HSKeys[kHw3HighSpeedBucketCount] =
            {"hw3HsPct0","hw3HsPct1","hw3HsPct2","hw3HsPct3","hw3HsPct4"};
        uint8_t hw3HSNew[kHw3HighSpeedBucketCount] = {0};
        bool    hw3HSHas[kHw3HighSpeedBucketCount] = {false};
        for (int i = 0; i < kHw3HighSpeedBucketCount; i++) {
            if (!req->hasParam(kHw3HSKeys[i])) continue;
            int p = req->getParam(kHw3HSKeys[i])->value().toInt();
            if (p < 0 || p > 50) { req->send(400, "text/plain", "bad hw3HsPct"); return; }
            hw3HSNew[i] = (uint8_t)p;
            hw3HSHas[i] = true;
        }

        bool changed = false;

        if (req->hasParam("fsdEnable")) {
            bool v = req->getParam("fsdEnable")->value().toInt() != 0;
            if (v != cfg.fsdEnable) { cfg.fsdEnable = v; changed = true; }
        }
        if (req->hasParam("hwMode")) {
            uint8_t v = (uint8_t)req->getParam("hwMode")->value().toInt();
            if (v != cfg.hwMode) { cfg.hwMode = v; changed = true; }
        }
        if (req->hasParam("speedProfile")) {
            uint8_t v = (uint8_t)req->getParam("speedProfile")->value().toInt();
            if (v != cfg.speedProfile) { cfg.speedProfile = v; changed = true; }
        }
        // If hwMode just transitioned away from HW4 and no fresh speedProfile was
        // supplied, clamp any stale HW4-only value (3/4) to 2 — otherwise HW3/Legacy
        // would silently operate on an out-of-range profile.
        if (cfg.hwMode != 2 && cfg.speedProfile > 2) {
            cfg.speedProfile = 2;
            changed = true;
        }
        if (req->hasParam("profileMode")) {
            bool v = req->getParam("profileMode")->value().toInt() != 0;
            if (v != cfg.profileModeAuto) { cfg.profileModeAuto = v; changed = true; }
        }
        if (req->hasParam("isaChime")) {
            bool v = req->getParam("isaChime")->value().toInt() != 0;
            if (v != cfg.isaChimeSuppress) { cfg.isaChimeSuppress = v; changed = true; }
        }
        if (req->hasParam("emergencyDet")) {
            bool v = req->getParam("emergencyDet")->value().toInt() != 0;
            if (v != cfg.emergencyDetection) { cfg.emergencyDetection = v; changed = true; }
        }
        if (req->hasParam("forceActivate")) {
            bool v = req->getParam("forceActivate")->value().toInt() != 0;
            if (v != cfg.forceActivate) { cfg.forceActivate = v; changed = true; }
        }
        if (req->hasParam("overrideSL")) {
            bool v = req->getParam("overrideSL")->value().toInt() != 0;
            if (v != cfg.overrideSpeedLimit) { cfg.overrideSpeedLimit = v; changed = true; }
        }
        if (req->hasParam("legacyOffset")) {
            int v = req->getParam("legacyOffset")->value().toInt();
            if (v < 0 || v > 33) { req->send(400, "text/plain", "bad legacyOffset"); return; }
            uint8_t nv = (uint8_t)v;
            if (nv != cfg.legacyOffset) { cfg.legacyOffset = nv; changed = true; }
        }
        if (req->hasParam("removeVSL")) {
            bool v = req->getParam("removeVSL")->value().toInt() != 0;
            if (v != cfg.removeVisionSpeedLimit) { cfg.removeVisionSpeedLimit = v; changed = true; }
        }
        if (req->hasParam("hw3AutoSpeed")) {
            bool v = req->getParam("hw3AutoSpeed")->value().toInt() != 0;
            if (v != cfg.hw3AutoSpeed) { cfg.hw3AutoSpeed = v; changed = true; }
        }
        if (req->hasParam("hw3CustomSpeed")) {
            bool v = req->getParam("hw3CustomSpeed")->value().toInt() != 0;
            if (v != cfg.hw3CustomSpeed) { cfg.hw3CustomSpeed = v; changed = true; }
        }
        if (req->hasParam("hw3OffsetSlew")) {
            bool v = req->getParam("hw3OffsetSlew")->value().toInt() != 0;
            if (v != cfg.hw3OffsetSlew) { cfg.hw3OffsetSlew = v; changed = true; }
        }
        if (req->hasParam("hw3SlewRate")) {
            int r = req->getParam("hw3SlewRate")->value().toInt();
            if (r < kHw3SlewRateMin) r = kHw3SlewRateMin;
            if (r > kHw3SlewRateMax) r = kHw3SlewRateMax;
            uint8_t nv = (uint8_t)r;
            if (nv != cfg.hw3SlewRatePctPerSec) { cfg.hw3SlewRatePctPerSec = nv; changed = true; }
        }
        if (req->hasParam("hw3HighSpeedEnable")) {
            bool v = req->getParam("hw3HighSpeedEnable")->value().toInt() != 0;
            if (v != cfg.hw3HighSpeedEnable) { cfg.hw3HighSpeedEnable = v; changed = true; }
        }
        if (req->hasParam("hw3WireEncoding")) {
            int v = req->getParam("hw3WireEncoding")->value().toInt();
            if (v < 0) v = 0;
            if (v > HW3_ENC_MAX) v = HW3_ENC_MAX;
            uint8_t nv = (uint8_t)v;
            if (nv != cfg.hw3WireEncoding) { cfg.hw3WireEncoding = nv; changed = true; }
        }
        if (req->hasParam("ipBlockerEnabled")) {
            bool v = req->getParam("ipBlockerEnabled")->value().toInt() != 0;
            if (v != cfg.ipBlockerEnabled) { cfg.ipBlockerEnabled = v; changed = true; }
        }
        if (req->hasParam("isaOverride")) {
            bool v = req->getParam("isaOverride")->value().toInt() != 0;
            if (v != cfg.isaOverride) { cfg.isaOverride = v; changed = true; }
        }
        // Apply pre-validated values — validate-all-then-apply preserves atomicity
        // so a late bad field can't leave earlier slots mutated in RAM.
        for (int i = 0; i < kHw3CustomTargetCount; i++) {
            if (!hw3CTHas[i]) continue;
            if (hw3CTNew[i] != cfg.hw3CustomTarget[i]) {
                cfg.hw3CustomTarget[i] = hw3CTNew[i];
                changed = true;
            }
        }
        for (int i = 0; i < kHw3HighSpeedBucketCount; i++) {
            if (!hw3HSHas[i]) continue;
            if (hw3HSNew[i] != cfg.hw3HighSpeedTargetPct[i]) {
                cfg.hw3HighSpeedTargetPct[i] = hw3HSNew[i];
                changed = true;
            }
        }
        // precond removed from UI — requires Vehicle CAN (X179 pin 9/10)
        if (req->hasParam("hw4Offset")) {
            int raw = req->getParam("hw4Offset")->value().toInt();
            if (raw < 0)  raw = 0;
            if (raw > 15) raw = 15;  // UI cap aligned with upstream Turkish fw; hardware field is 6 bits but >15 unverified
            uint8_t v = (uint8_t)raw;
            if (v != cfg.hw4OffsetRaw) { cfg.hw4OffsetRaw = v; changed = true; }
        }
        if (req->hasParam("trackMode")) {
            bool v = req->getParam("trackMode")->value().toInt() != 0;
            if (v != cfg.trackModeEnable) { cfg.trackModeEnable = v; changed = true; }
        }

        if (changed) saveConfig();
        req->send(200, "text/plain", "OK");
    });

